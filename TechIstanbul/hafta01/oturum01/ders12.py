# atama operatörleri

sayi = 10
print(sayi) # 10

# sayi = sayi * 2
sayi *= 2
print(sayi) # 20

# sayi = sayi + 10
sayi += 10
print(sayi) # 30

sayi -= 5
print(sayi) # 25

sayi **= 2
print(sayi) # 625

sayi %= 10
print(sayi) # 5

sayi %= 3
print(sayi) # 2

sayi /= 6
print(sayi) # 0.3333333333333333

