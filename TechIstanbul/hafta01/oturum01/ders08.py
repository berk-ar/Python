# karşılaştırma operatörleri

# iki eşittir yani a == b => a ve b eşit mi anlamına gelir
print("tech" == "istanbul") # false
print("istanbul" == "istanbul") # true

print(9 == 5) # false
print(9 != 5) # true # eşit değil

print(8 <= 9) # true
print(8 < 9) # true

print(88 > 55)
print(88 >= 95)

print("ehliyet alma durumunuz", 19 >= 18) # true
print("ehliyet alma durumunuz", 5 >= 18) # false